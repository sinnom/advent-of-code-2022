use itertools::Itertools;

fn main() {
    let answer: u32 = get_input()
        .split('\n')
        .filter_map(|round| round.split(' ').collect_tuple())
        .map(|(theirs, outcome)| get_round_points(outcome, theirs))
        .sum();

    println!("{answer}")
}

fn get_round_points<T, U>(outcome: U, theirs: T) -> u32
where
    Choice: From<T>,
    Outcome: From<U>,
{
    let outcome = Outcome::from(outcome);
    let theirs = Choice::from(theirs);
    outcome.choice_vs(theirs) as u32 + outcome as u32
}

enum Choice {
    Rock = 1,
    Paper = 2,
    Scissors = 3,
}

enum Outcome {
    Loss = 0,
    Draw = 3,
    Win = 6,
}

impl Outcome {
    fn choice_vs(&self, other: Choice) -> Choice {
        match self {
            Self::Draw => other,
            Self::Loss => match other {
                Choice::Rock => Choice::Scissors,
                Choice::Paper => Choice::Rock,
                Choice::Scissors => Choice::Paper,
            },
            Self::Win => match other {
                Choice::Rock => Choice::Paper,
                Choice::Paper => Choice::Scissors,
                Choice::Scissors => Choice::Rock,
            },
        }
    }
}

impl From<&str> for Outcome {
    fn from(char: &str) -> Self {
        match char {
            "X" => Self::Loss,
            "Y" => Self::Draw,
            "Z" => Self::Win,
            _ => panic!("Strategy guide used an invalid character: {:?}", char),
        }
    }
}

impl Choice {
    fn outcome_vs(&self, other: Choice) -> Outcome {
        match self {
            Self::Rock => match other {
                Self::Rock => Outcome::Draw,
                Self::Paper => Outcome::Loss,
                Self::Scissors => Outcome::Win,
            },
            Self::Paper => match other {
                Self::Rock => Outcome::Win,
                Self::Paper => Outcome::Draw,
                Self::Scissors => Outcome::Loss,
            },
            Self::Scissors => match other {
                Self::Rock => Outcome::Loss,
                Self::Paper => Outcome::Win,
                Self::Scissors => Outcome::Draw,
            },
        }
    }
}

impl From<&str> for Choice {
    fn from(c: &str) -> Self {
        match c {
            "A" => Self::Rock,
            "B" => Self::Paper,
            "C" => Self::Scissors,
            "X" => Self::Rock,
            "Y" => Self::Paper,
            "Z" => Self::Scissors,
            _ => panic!("Strategy guide used an invalid character: {:?}", c),
        }
    }
}

